alphabet = "abcdefghijklmnopqrstuvwxyz"
myString = "I really like music, it's very calming.\n"

# print(alphabet[0])
# print(alphabet[-1])

# Slicing
print(alphabet[0:4]) # Slice UP TO second index.
print(myString[0:5])
# alphabet [start:stop]

# Slide from the Start
print(alphabet[:7])
print(myString[:14])

# Slice to the End
print(alphabet[12:])
print(myString[6:])

# Slice the whole %#$#! Thing
print(alphabet[:])
print(myString[:])

# Negative Index Slicing
print(alphabet[-4:-1])
print(myString[-7:2])